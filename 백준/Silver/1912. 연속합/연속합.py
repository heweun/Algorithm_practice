#n개의 정수로 이루어진 임의의 수열이 주어짐
#연속된 몇 개의 수를 선택해서 구할 수 있는 합 중 가장 큰 합 *한 개 이상 선택

n,*m = map(int,open(0).read().split())

for i in range(1, n):
 m[i] = max(m[i], m[i] + m[i - 1]) 
 #업데이트 한다고 생각
 #원래 m[i]에 있던 값과 이전 값과 더해진 값 중 더 큰 값으로 대체->다음 숫자도 앞 숫자에 영향받기 때문
 #print(f'm[i]:{m[i]}, m:{m}')

print(max(m))